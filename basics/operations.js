//prefix and postfix increment

//prefix opeator: first increment the given operand and then performs further operation

let x = 3;
let y = ++x;
console.log("hey", x)
console.log("bye", y) //increments x and then assign's the incremented value

//postfix operator : first performs operation then increments the number we have given increment to

let a = 3;
let b = a++; //assigns a's value then increments
console.log("hey", a)
console.log("bye", b)


















